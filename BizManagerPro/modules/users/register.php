<?php
require_once '../../config/database.php';
require_once '../../config/session.php';

$error = '';
$success = '';
$username = '';
$email = '';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $username = sanitize($_POST['username'] ?? '');
    $email = sanitize($_POST['email'] ?? '');
    $password = $_POST['password'] ?? '';
    $confirm_password = $_POST['confirm_password'] ?? '';
    $role = 'owner'; // Default role

    // Validation
    if (empty($username) || empty($email) || empty($password)) {
        $error = 'All fields are required.';
    } elseif ($password !== $confirm_password) {
        $error = 'Passwords do not match.';
    } elseif (strlen($password) < 6) {
        $error = 'Password must be at least 6 characters.';
    } else {
        // Check if username or email already exists
        $check_query = "SELECT user_id FROM users WHERE username = ? OR email = ?";
        $result = executeQuery($conn, $check_query, "ss", [$username, $email]);
        
        if ($result['success']) {
            $stmt = $result['stmt'];
            $stmt->store_result();
            
            if ($stmt->num_rows > 0) {
                $error = 'Username or email already exists.';
            } else {
                // Hash password
                $hashed_password = password_hash($password, PASSWORD_BCRYPT);
                
                // Insert new user
                $insert_query = "INSERT INTO users (username, password, email, role) VALUES (?, ?, ?, ?)";
                $insert_result = executeQuery($conn, $insert_query, "ssss", [$username, $hashed_password, $email, $role]);
                
                if ($insert_result['success']) {
                    $success = 'Registration successful! You can now login.';
                    // Clear form
                    $username = $email = '';
                } else {
                    $error = 'Registration failed. Please try again.';
                }
            }
            $stmt->close();
        } else {
            $error = $result['error'];
        }
    }
}
